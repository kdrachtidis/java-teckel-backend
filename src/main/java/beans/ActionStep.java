package beans;

public class ActionStep {
	private String ActionStepID;
	private String ActionStepTitle;
	private String ActionStepDescription;
	
	public String getActionStepID() {
		return ActionStepID;
	}
	public void setActionStepID(String actionStepID) {
		ActionStepID = actionStepID;
	}
	public String getActionStepTitle() {
		return ActionStepTitle;
	}
	public void setActionStepTitle(String actionStepTitle) {
		ActionStepTitle = actionStepTitle;
	}
	public String getActionStepDescription() {
		return ActionStepDescription;
	}
	public void setActionStepDescription(String actionStepDescription) {
		ActionStepDescription = actionStepDescription;
	}
}
