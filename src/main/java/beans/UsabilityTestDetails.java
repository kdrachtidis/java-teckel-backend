package beans;

public class UsabilityTestDetails {
	private String UsabilityTestID;
	private String UsabilityTestTitle;
	private String UsabilityTestDescription;
	private String UsabilityTestPrototype;
	private String UsabilityTestEvent;
	private String UsabilityTestEventDate;
	
	public String getUsabilityTestID() {
		return UsabilityTestID;
	}
	public void setUsabilityTestID(String usabilityTestID) {
		UsabilityTestID = usabilityTestID;
	}
	public String getUsabilityTestTitle() {
		return UsabilityTestTitle;
	}
	public void setUsabilityTestTitle(String usabilityTestTitle) {
		UsabilityTestTitle = usabilityTestTitle;
	}
	public String getUsabilityTestDescription() {
		return UsabilityTestDescription;
	}
	public void setUsabilityTestDescription(String usabilityTestDescription) {
		UsabilityTestDescription = usabilityTestDescription;
	}
	public String getUsabilityTestPrototype() {
		return UsabilityTestPrototype;
	}
	public void setUsabilityTestPrototype(String usabilityTestPrototype) {
		UsabilityTestPrototype = usabilityTestPrototype;
	}
	public String getUsabilityTestEvent() {
		return UsabilityTestEvent;
	}
	public void setUsabilityTestEvent(String usabilityTestEvent) {
		UsabilityTestEvent = usabilityTestEvent;
	}
	public String getUsabilityTestEventDate() {
		return UsabilityTestEventDate;
	}
	public void setUsabilityTestEventDate(String usabilityTestEventDate) {
		UsabilityTestEventDate = usabilityTestEventDate;
	}	
}
